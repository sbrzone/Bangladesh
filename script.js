const mainContent = document.getElementById('mainContent');
const searchInput = document.getElementById('searchInput');

function render(data) {
    mainContent.innerHTML = '';
    data.forEach(div => {
        const divCard = document.createElement('div');
        divCard.className = 'division-card';
        
        const districtsHTML = div.districts.map(dist => `
            <div class="district-box">
                <div class="dist-name">${dist.name}</div>
                <div class="upazila-list">
                    ${dist.upazilas.map(up => `<span class="up-tag">${up}</span>`).join('')}
                </div>
            </div>
        `).join('');

        divCard.innerHTML = `
            <div class="div-header">
                <span>${div.division} বিভাগ</span>
                <span>${div.districts.length}টি জেলা</span>
            </div>
            <div class="district-grid">${districtsHTML}</div>
        `;
        mainContent.appendChild(divCard);
    });
}

// Initial render
render(bdData);

// Search functionality
searchInput.addEventListener('input', (e) => {
    const term = e.target.value.toLowerCase();
    const filtered = bdData.map(div => ({
        ...div,
        districts: div.districts.filter(dist => 
            dist.name.toLowerCase().includes(term) || 
            dist.upazilas.some(u => u.toLowerCase().includes(term))
        )
    })).filter(div => div.districts.length > 0);
    render(filtered);
});
